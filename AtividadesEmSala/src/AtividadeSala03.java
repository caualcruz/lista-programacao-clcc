public class AtividadeSala03 {
    public static void main(String[] args){
        int a = 15;
        int b = 20;
        int resultado = 0;

        resultado = a+b;
        System.out.println("A soma dos números é de: " + resultado);
        resultado = a-b;
        System.out.println("A subtração dos números é de: " + resultado);
        resultado = a*b;
        System.out.println("A multiplicação dos números é de: " + resultado);
        resultado = a/b;
        System.out.println("A divisão dos números é de: " + resultado);
        System.out.println("------------------------------------------------");
        System.out.println("a == b = " + (a == b));
        System.out.println("a != b = " + (a != b));
        System.out.println("a >= b = " + (a >= b));
        System.out.println("a <= b = " + (a <= b));
    }
}
